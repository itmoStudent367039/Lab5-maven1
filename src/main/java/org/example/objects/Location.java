package org.example.objects;

public class Location {
    private double x;
    private long y;
    private float z;
    private String name;

    public String getName() {
        return name;
    }

    public float getZ() {
        return z;
    }

    public long getY() {
        return y;
    }

    public double getX() {
        return x;
    }
}
